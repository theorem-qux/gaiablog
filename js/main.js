function formatDate(dateStr) {
  if (!dateStr) return '';
  const [y, m, d] = dateStr.split('-');
  return `${y}年${parseInt(m)}月${parseInt(d)}日`;
}

function zeroPad(n) {
  return String(n).padStart(3, '0');
}

// インデックスページ用：記事カードを生成
function renderArticleList() {
  const container = document.getElementById('article-list');
  if (!container) return;

  ARTICLES.forEach(article => {
    const no = article.no;
    const isCursed = no >= 101;
    const card = document.createElement('article');
    card.className = 'article-card' + (isCursed ? ' cursed' : '');

    const title = article.title || '';
    const dateStr = article.date ? formatDate(article.date) : '';
    const excerpt = article.excerpt || '';

    card.innerHTML = `
      <div class="article-number">No.${no}</div>
      ${title ? `<div class="article-card-title"><a href="articles/no-${zeroPad(no)}.html">${title}</a></div>` : `<div class="article-card-title"><a href="articles/no-${zeroPad(no)}.html"></a></div>`}
      ${dateStr ? `<div class="article-meta"><span>${dateStr}</span></div>` : ''}
      ${excerpt ? `<div class="article-excerpt">${excerpt}</div>` : ''}
    `;

    container.appendChild(card);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  renderArticleList();
});
